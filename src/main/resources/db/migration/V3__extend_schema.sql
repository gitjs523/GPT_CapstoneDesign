-- 5. qa_history
-- notebook_id: 노트북 Q&A(26번)일 때 설정, 해설 기반 Q&A(45번)일 때도 설정 가능
-- question_id: 해설 기반 Q&A(45번)일 때 설정, 노트북 Q&A(26번)일 때 NULL
-- source_section_ids: 답변 근거로 사용된 Section ID 목록 (retrieval 결과)
CREATE TABLE qa_history (
    qa_history_id      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id            BIGINT NOT NULL,
    notebook_id        BIGINT NULL,
    question_id        BIGINT NULL,
    user_question      TEXT NOT NULL,
    ai_answer          TEXT NULL,
    answerable         BOOLEAN NOT NULL DEFAULT TRUE,
    source_section_ids INTEGER[] NULL,
    created_at         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_qa_history_user     FOREIGN KEY (user_id)     REFERENCES users              (user_id),
    CONSTRAINT fk_qa_history_notebook FOREIGN KEY (notebook_id) REFERENCES notebook           (notebook_id),
    CONSTRAINT fk_qa_history_question FOREIGN KEY (question_id) REFERENCES generated_question (question_id)
);

CREATE INDEX idx_qa_history_user_id     ON qa_history (user_id);
CREATE INDEX idx_qa_history_notebook_id ON qa_history (notebook_id);
CREATE INDEX idx_qa_history_question_id ON qa_history (question_id);
