-- V3: 스키마 확장
--   1. notebook   : is_default, deleted_at 추가
--   2. document   : summary, deleted_at 추가
--   3. generated_question : is_edited, deleted_at 추가
--   4. chunk      : embedding vector(1024) 추가 (Parent-Child Retrieval — Chunk이 벡터 검색 단위)
--   5. qa_history : 노트북 Q&A + 해설 기반 Q&A 통합 테이블 신규 생성

-- 1. notebook
ALTER TABLE notebook
    ADD COLUMN is_default BOOLEAN NOT NULL DEFAULT FALSE,
    ADD COLUMN deleted_at TIMESTAMP NULL;

-- 2. document
ALTER TABLE document
    ADD COLUMN summary    TEXT NULL,
    ADD COLUMN deleted_at TIMESTAMP NULL;

-- 3. generated_question
ALTER TABLE generated_question
    ADD COLUMN is_edited  BOOLEAN NOT NULL DEFAULT FALSE,
    ADD COLUMN deleted_at TIMESTAMP NULL;

-- 4. chunk embedding
-- qwen3-embedding:0.6b 기준 1024차원.
-- 팀원 PC에서 curl로 실제 차원 확인 후 불일치 시 V4 migration으로 수정.
ALTER TABLE chunk
    ADD COLUMN embedding vector(1024) NULL;

-- ivfflat 인덱스는 충분한 데이터가 쌓인 뒤 별도 migration으로 추가 권장.
-- (데이터 없는 상태에서 CREATE INDEX USING ivfflat 시 recall 품질 보장 불가)
-- 추후 추가 예시:
